    xor ebx, ebx
cLoop1:
    xor ecx, ecx
cLoop2:
    invoke getSquare, ebx, ecx
    test BYTE PTR [eax], STORAGE
    jz cDone
    test BYTE PTR [eax], BOX
    jnz cDone
    xor eax, eax
    ret
cDone:
    inc ecx
    cmp ecx, width_
    jl cLoop2
    inc ebx
    cmp ebx, height
    jl cLoop1
    ret
completed endp

drawWelcome proc
    invoke printf, OFFSET moveToStart
    invoke printf, OFFSET logo
    invoke printf, OFFSET newLine
    mov ecx, 1
dwLoop:
    push ecx
    cmp ecx, level
    jne dwElse
    invoke printf, OFFSET levelSelected
    jmp dwDone1
dwElse:
    invoke printf, OFFSET levelNotSelected
dwDone1:
    pop ecx
    mov eax, ecx
    cdq
    mov ebx, LEVELS_PER_ROW
    div ebx
    test edx, edx
    jnz dwDone2
    push ecx
    invoke printf, OFFSET newLine
    pop ecx
dwDone2:
    inc ecx
    cmp ecx, LEVELS
    jle dwLoop
    ret
drawWelcome endp

drawSquare proc C x:DWORD, y:DWORD
    mov eax, playerX
    cmp eax, x
    jne dsDone1
    mov eax, playerY
    cmp eax, y
    jne dsDone1
    invoke printf, OFFSET player
    ret
dsDone1:
    invoke getSquare, x, y
    mov al, BYTE PTR [eax]
    test al, WALL
    jz dsDone2
    invoke printf, OFFSET wall
    ret
dsDone2:
    test al, BOX
    jz dsDone3
    test al, STORAGE
    jz dsDone3
    invoke printf, OFFSET boxStorage
    ret
dsDone3:
    test al, BOX
    jz dsDone4
    invoke printf, OFFSET box
    ret
dsDone4:
    test al, STORAGE
    jz dsDone5
    invoke printf, OFFSET storage
    ret
dsDone5:
    invoke printf, OFFSET empty
    ret
drawSquare endp

drawMap proc
    invoke printf, OFFSET moveToStart
    xor ebx, ebx
dmLoop1:
    xor ecx, ecx
dmLoop2:
    push ebx
    push ecx
    invoke drawSquare, ebx, ecx
    pop ecx
    pop ebx
    inc ecx
    cmp ecx, width_
    jl dmLoop2
    push ebx
    invoke printf, OFFSET newLine
    pop ebx
    inc ebx
    cmp ebx, height
    jl dmLoop1
    ret
drawMap endp

start:
    invoke SetConsoleOutputCP, 65001
    invoke printf, OFFSET warning
    invoke printf, OFFSET clear

loop1:
    invoke drawWelcome
    invoke _getch
    cmp eax, INTERRUPT
    jne done1
    ret
done1:
    cmp eax, UP
    jne done2
    cmp level, LEVELS_PER_ROW
    jle done2
    sub level, LEVELS_PER_ROW
    jmp loop1
done2:
    cmp eax, DOWN
    jne done3
    cmp level, LEVELS-LEVELS_PER_ROW
    jg done3
    add level, LEVELS_PER_ROW
    jmp loop1
done3:
    cmp eax, LEFT
    jne done4
    cmp level, 1
    je done4
    dec level
    jmp loop1
done4:
    cmp eax, RIGHT
    jne done5
    cmp level, LEVELS
    je done5
    inc level
    jmp loop1
done5:
    cmp eax, RETURN
    jne done6
    jmp end1
done6:
    jmp loop1
end1:

    mov eax, level
    dec eax
    imul eax, 20
    add eax, OFFSET levels
    mov edx, DWORD PTR [eax]
    mov map, edx
    mov edx, DWORD PTR [eax+4]
    mov height, edx
    mov edx, DWORD PTR [eax+8]
    mov width_, edx
    mov edx, DWORD PTR [eax+12]
    mov playerX, edx
    mov edx, DWORD PTR [eax+16]
    mov playerY, edx
    invoke printf, OFFSET clear

loop2:
    invoke drawMap
    invoke completed
    test eax, eax
    jz done7
    invoke printf, OFFSET solved
    ret
done7:
    invoke _getch
    cmp eax, INTERRUPT
    jne done8
    ret
done8:
    cmp eax, UP
    jne done9
    invoke pushUp
    jmp loop2
done9:
    cmp eax, DOWN
    jne done10
    invoke pushDown
    jmp loop2
done10:
    cmp eax, LEFT
    jne done11
    invoke pushLeft
    jmp loop2
done11:
    cmp eax, RIGHT
    jne done12
    invoke pushRight
    jmp loop2
done12:
    jmp loop2
end2:

    ret
end start
